        <h2 class="text-center mb-4">Marco Teórico - Supermercado Yumara</h2>

        <!-- Descripción General del Proyecto -->
        <h3>Descripción General del Proyecto</h3>
        <p>
            El proyecto tiene como objetivo diseñar un sistema completo de gestión y administración de ventas para el supermercado <strong>Supermercado Yumara</strong>, ubicado en la ciudad de La Paz, Bolivia. Este sistema está enfocado en optimizar el control de inventarios, la gestión de ventas, y la relación con proveedores, lo cual facilitará la operación eficiente del supermercado en sus 8 sucursales.
        </p>

        <!-- Objetivo -->
        <h3>Objetivo</h3>
        <p>
            El propósito de este análisis y diseño del sistema es proveer una solución integral que permita gestionar de manera eficiente el inventario de productos, las ventas diarias y la interacción con proveedores, garantizando el abastecimiento adecuado y un flujo continuo de productos en las sucursales.
        </p>

        <!-- Alcance -->
        <h3>Alcance</h3>
        <p>
            El sistema abarcará varias funciones clave:
            <ul>
                <li><strong>Gestión de Ventas:</strong> Control y registro de transacciones de ventas, con la capacidad de generar reportes detallados de las mismas.</li>
                <li><strong>Administración de Proveedores:</strong> Sistema de gestión de proveedores que permita realizar órdenes de compra de insumos y productos.</li>
                <li><strong>Control de Inventarios:</strong> Actualización automática de inventarios a medida que se realicen ventas, así como un sistema de alertas para reordenar productos cuando el stock sea bajo.</li>
            </ul>
        </p>


        <!-- Información del Supermercado -->
        <h3>Información del Supermercado Yumara</h3>
        <p>
            El <strong>Supermercado Yumara</strong> se encuentra en <strong>Av. Arce NRO 2519 Condominio torres del poeta esq. plaza Isabel La Catolica, La Paz, Municipio Murillo, Bolivia</strong>. Con 5 sucursales en diferentes zonas de la ciudad, se dedica a ofrecer una amplia variedad de productos a sus más de 5,000 clientes registrados.
        </p>
        <ul>
            <li><strong>Categorías de Productos:</strong> Alimentos, bebidas, productos de limpieza, artículos de hogar.</li>
            <li><strong>Perfil de Clientes:</strong> Consumidores finales y pequeñas empresas que realizan compras al por mayor.</li>
            <li><strong>Relación con Proveedores:</strong> El supermercado mantiene relaciones continuas con proveedores nacionales e internacionales para garantizar la disponibilidad de productos.</li>
        </ul>
